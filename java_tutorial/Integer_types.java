public class Integer_types {
    public static void main(String[] args) {
        byte myByte = 100; // byte, 1 byte = 8 bits, 255 combinations, -128 to 127
        System.out.println(myByte);
        short myShort = 5000; // short, 2 byte = 16 bits, 65536 combinations, -32768 to 32768
        System.out.println(myShort);
        int myInt = 100000; // int, 4 bytes = 32 bits, 4,29,49,67,296 combinations, -2,14,74,83,648 to 2,14,74,83,647
        System.out.println(myInt);
        long myLong = 1000000000000L; // long, 8 bytes = 64 bits, 1,84,46,74,40,73,70,95,51,616 combinations, -92,23,37,20,36,85,47,75,808 to 92,23,37,20,36,85,47,75,807
        System.out.println(myLong);
    }


}
