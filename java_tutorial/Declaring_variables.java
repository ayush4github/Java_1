public class Declaring_variables {
    public static void main(String[] args){
        int myNum = 15;
        String name;
        char first; 
        float float_Value;
        boolean bool_Value;
        double double_Value;
        double_Value = 0.4587;
        name = "John";
        first = 'A';
        float_Value = 19.99f;
        bool_Value = true;
        System.out.println(myNum + " " + name + " " + first + " " + float_Value + " " + bool_Value + " " + double_Value);
    }
}